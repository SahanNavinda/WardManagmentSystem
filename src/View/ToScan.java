    public void add(){
       try{ stmt = DBConnection.getStatementConnection(); 
           
            rs=stmt.executeQuery("Select bednumber from addscan");
             
            while(rs.next()){
                combobed.addItem(rs.getString("bednumber"));
            }
   }
       catch(SQLException ex){ 
           Logger.getLogger(DBSearch.class.getName()).log(Level.SEVERE, null, ex);
           
} }
     public void search(String beds){   
        try{ stmt = DBConnection.getStatementConnection(); 
           
            rs=stmt.executeQuery("Select pname,description from `addscan` WHERE bednumber = "+beds+"");
               while(rs.next()){ String des= rs.getString("description");
               String name= rs.getString("pname");
               pname.setText(name);
                desTF.setText(des);
                
            }}
            catch(SQLException ex){ 
           Logger.getLogger(DBSearch.class.getName()).log(Level.SEVERE, null, ex);
         
            }
}
